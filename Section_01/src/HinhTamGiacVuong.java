
public class HinhTamGiacVuong {
	public static double chuVi(double canhGocVuong1, double canhGocVuong2, double canhHuyen) {
		return canhGocVuong1 + canhGocVuong2 + canhHuyen;
	}

	public static double dienTich(double canhGocVuong1, double canhGocVuong2, double canhHuyen) {
		return (canhGocVuong1 * canhGocVuong2) / 2;
	}
}
